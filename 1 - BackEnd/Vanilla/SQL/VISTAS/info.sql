/* En SQL, una vista es una tabla virtual basada en el conjunto de resultados de una instrucción SQL.
Una vista contiene filas y columnas, como una tabla real. Los campos de una vista son campos 
de una o más tablas reales de la base de datos.

NOTA: ¡Una vista siempre muestra datos actualizados! El motor de la base de datos recrea la vista 
cada vez que un usuario la consulta. */