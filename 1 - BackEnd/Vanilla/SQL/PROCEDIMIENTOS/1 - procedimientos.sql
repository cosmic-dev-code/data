/* ######===--- ¿Qué es un procedimiento? ---===###### */
/* ##########===============================########## */

/* Un procedimiento almacenado es un código SQL preparado que puede guardar, por lo que el código se puede 
reutilizar una y otra vez. Entonces, si tiene una consulta SQL que escribe una y otra vez, guárdela como 
un procedimiento almacenado y luego simplemente llámela para ejecutarla. 

También puede pasar parámetros a un procedimiento almacenado, de modo que el procedimiento 
almacenado pueda actuar en función de los valores de parámetro que se pasan. */