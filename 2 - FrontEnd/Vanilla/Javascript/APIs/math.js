// Retorna la raiz cuadrada
Math.sqrt(5);

// Retorna la raiz cubica.
Math.cbrt(5);

// Devuelve el numero mas grande de la lista.
Math.max(4,5,1,7,9,10,3);

// Devuelve el numero mas pequeño de la lista.
Math.min(4,5,1,7,9,10,3);

/* Devuelve un numero random entre 1 y 0, 
(con muchos decimales). */
Math.random();

/* Devuelve un numero random entre 0 y 100, (con muchos decimales). */
Math.random()*100;

// Redondea al numero mas proximo.
Math.ceil(44.55);

/* Redondea al numero mas cercano, (de 5 hacia arriba sube, de 4 hacia abajo solo quita los decimales). */
Math.round(44.5);

// Redondea hacia abajo quitando los decimales.
Math.floor(4.9999999);

// Redonde hacia arriba solo con esta cantidad de decimales.
Math.floor(4.9999999999999999);

// Redondea al número más cercano (4.muchos decimales).
Math.fround(4.9999);

/* Con esta cantidad de decimales redondea 
al siguiente numero quitando los decimales. */
Math.fround(4.9999999);

/* Elimina el resto de los decimales. */
Math.trunc(4.99999999999);

/* Con esta cantidad de decimales el metodo redondea 
el numero al siguiente y elimina los decimales. */
Math.trunc(4.9999999999999999);

/* Eleva la base al exponente. */
Math.pow(3, 5); // 3 al exponente 5.

